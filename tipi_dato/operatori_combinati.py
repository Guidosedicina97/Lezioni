# dichiarazione e istanziazione di 2 variabili
numero = 10
stringa = "ciao"

# vogliamo aggiungere 5 al valore della variabile numero
numero = numero + 5 # versione prolissa
numero += 5 # versione ottimizzata
print(numero)

# vogliamo concatenare"mondo" al valore della variabile stringa
stringa = stringa + " mondo" # versione prolissa
stringa += " mondo" # versione ottimizzata
print(stringa)

